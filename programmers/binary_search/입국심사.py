def solution(n, times):
    # answer:
    # "현재까지 찾은 가능한 시간들 중 가장 작은 후보"
    # 처음엔 아직 찾은 값이 없어서 0으로 둔다.
    answer = 0

    # left:
    # 탐색 가능한 최소 시간
    # 심사 시간은 0분일 수 없으니 1부터 시작
    left = 1

    # right:
    # 탐색 가능한 최대 시간(상한)
    # 최악의 경우 가장 느린 심사관이 n명을 전부 처리한다고 가정하면
    # max(times) * n 시간 안에는 반드시 끝난다.
    # 그래서 이 값은 "무조건 가능한 시간"인 안전한 상한이 된다.
    right = max(times) * n

    # 이분탐색:
    # [left, right] 범위 안에서
    # "n명을 모두 처리할 수 있는 최소 시간"을 찾는다.
    while left <= right:
        # mid:
        # 이번에 검사해볼 후보 시간
        mid = (left + right) // 2

        # total:
        # mid 시간 동안 전체 심사관이 처리할 수 있는 사람 수
        #
        # 예:
        # 심사관 한 명의 처리 시간이 t라면
        # mid 시간 동안 처리 가능한 사람 수는 mid // t
        #
        # 예를 들어 t=7, mid=20이면
        # 20분 동안 2명 처리 가능 (3명은 못 끝냄)
        #
        # 이 값을 모든 심사관에 대해 더하면
        # mid 시간 안에 총 몇 명 처리 가능한지 알 수 있다.
        total = sum(mid // t for t in times)

        # 만약 total >= n 이라면,
        # mid 시간 안에 n명 이상 처리 가능하다는 뜻
        # 즉, "이 시간은 가능한 시간"이다.
        if total >= n:
            # 가능한 시간이므로 정답 후보로 저장
            answer = mid

            # 그런데 우리는 "최소 시간"을 찾아야 하므로
            # 더 작은 시간으로도 가능한지 왼쪽 구간을 계속 탐색
            right = mid - 1
        else:
            # total < n 이라면
            # mid 시간 안에는 n명을 다 처리할 수 없다는 뜻
            # 즉, 시간이 부족하다.
            #
            # 따라서 더 큰 시간이 필요하므로
            # 탐색 범위를 오른쪽으로 옮긴다.
            left = mid + 1

    # while이 끝났다는 것은
    # 가능한 시간들 중 가장 작은 값을 answer에 저장해뒀다는 뜻
    return answer